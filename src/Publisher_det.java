    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        seteditfalse();
        String code="select * from publisher;";
           ob.execute(code);
           DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
           model.setRowCount(0);
           String[] s=new String[3];
           try{
               while(ob.rs.next()){
                   s[0]=ob.rs.getString(1);
                   s[1]=ob.rs.getString(2);
                   s[2]=ob.rs.getString(3);
                   model.addRow(s);
               }
           }
           catch(Exception e){
               System.out.println("error in getting data");
           }
       
        
    }//GEN-LAST:event_formWindowActivated

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
         String PID="",name="",contact="";
        PID=txtPID.getText();
        name=txtname.getText();
        contact=txtcontact.getText();
        if(PID.length()==0||name.length()==0||contact.length()==0){
            JOptionPane.showMessageDialog(this,"Please enter every detail");
        }
        else{
            String sql="insert into publisher values('"+PID+"','"+name+"','"+contact+"');";
            int flag=ob.update(sql);
        
        if(flag==1){
            JOptionPane.showMessageDialog(null,"record added successfully");
            new Publisher_det().setVisible(true);
            this.dispose();
         }
        else if(flag==0)
                JOptionPane.showMessageDialog(null,"record addition failed");
        seteditfalse();
        }
    }//GEN-LAST:event_btnsaveActionPerformed

